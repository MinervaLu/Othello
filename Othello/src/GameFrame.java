    private void bMouseClicked(java.awt.event.MouseEvent evt, int row, int col) {                                 
    	if(isvalid){
    	for(int i=0;i<8;i++){
    		for(int j=0;j<8;j++){
    			if(b.state[i][j]==0)
    				jb[i][j].setText("");
    		}
    	}
    	}
    	ArrayList<Disk> valid=new ArrayList<Disk>();
    	valid=b.validMove(color); //black
    	Disk move=new Disk(row,col,color);
        
    	if(have(valid,move)){
        isvalid=true;
       // jb[row][col].setForeground(color==1?Color.white:Color.black);
        jb[row][col].setBackground(color==1?Color.white:Color.black);
       // jb[row][col].setText("O");
        color=-color;//white
        b.add(move);
        b.update(move);
        printBoard(b.state);
        System.out.println();
        for(int i=0;i<8;i++){
        	for (int j=0;j<8;j++){
        		if(b.state[i][j]!=0){
        			jb[i][j].setBackground(b.state[i][j]==1?Color.white:Color.black);
        			//jb[i][j].setForeground(b.state[i][j]==1?Color.white:Color.black);
        		}
        	}
        }
        valid.clear();
        System.out.println(valid.isEmpty());
        valid=b.validMove(color);
        printDisk(valid);
        System.out.println();
        for (int i=0;i<valid.size();i++){
        	Disk disk=valid.get(i);
        	int r=disk.row;
        	int c=disk.col;
        	if(b.state[r][c]==0)
        	jb[r][c].setText("Valid");
        	jb[r][c].setForeground(color==1?Color.white:Color.black);
        }
        }
        else{
        	isvalid=false;
        	System.out.println("invalid");
        	JOptionPane pane = new JOptionPane("It's an invalid move");
        	pane.showMessageDialog(null,"Invalid move, try a different location");
        }
    	
      	
    	
    	
    } 
    
    
   

