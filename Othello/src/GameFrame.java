    public static boolean have(ArrayList<Disk> list,Disk d){
    	for (int i=0;i<list.size();i++){
    		if(list.get(i).row==d.row&&list.get(i).col==d.col&&list.get(i).color==d.color)
    			return true;
    	}
    	return false;
    }
    
    

